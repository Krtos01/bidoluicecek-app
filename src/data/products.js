// Resim import helper fonksiyonu
export const getProductImagePath = (imageName) => {
  // Resim dosya adını normalize et
  const normalizedName = imageName
    .toLowerCase()
    .replace(/\s+/g, '-')
    .replace(/[^a-z0-9-]/g, '');
  
  return `/images/${normalizedName}.jpg`;
};

